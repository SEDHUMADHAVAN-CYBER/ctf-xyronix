"""
Sentrix Browser - Gmail Integration Module
Secure OAuth2 authentication and email automation
"""

import json
from pathlib import Path
from typing import Optional, List
from datetime import datetime
import base64
from email.mime.text import MIMEText
from email.mime.multipart import MIMEMultipart

from google.oauth2.credentials import Credentials
from google.oauth2.flows import InstalledAppFlow
from google.auth.transport.requests import Request
from googleapiclient.discovery import build
from googleapiclient.errors import HttpError

SENTRIX_HOME = Path.home() / ".sentrix"
GMAIL_TOKEN_FILE = SENTRIX_HOME / "gmail_token.json"
GMAIL_CREDENTIALS_FILE = SENTRIX_HOME / "gmail_credentials.json"

# Gmail API scopes
SCOPES = [
    'https://www.googleapis.com/auth/gmail.send',
    'https://www.googleapis.com/auth/gmail.readonly'
]

class GmailIntegration:
    """Secure Gmail integration with OAuth2 authentication"""
    
    def __init__(self):
        self.service = None
        self.creds = None
        
    def authenticate(self) -> bool:
        """Authenticate with Gmail using OAuth2 flow"""
        self.creds = None
        
        # Load existing token
        if GMAIL_TOKEN_FILE.exists():
            self.creds = Credentials.from_authorized_user_file(
                GMAIL_TOKEN_FILE, SCOPES
            )
        
        # Refresh or re-authenticate
        if not self.creds or not self.creds.valid:
            if self.creds and self.creds.expired and self.creds.refresh_token:
                try:
                    self.creds.refresh(Request())
                except Exception as e:
                    print(f"Token refresh failed: {e}")
                    self.creds = None
            
            if not self.creds:
                # Run OAuth2 flow
                if not GMAIL_CREDENTIALS_FILE.exists():
                    print("Error: Gmail credentials file not found.")
                    print("Please download credentials from Google Cloud Console")
                    print("and save as gmail_credentials.json in ~/.sentrix/")
                    return False
                
                flow = InstalledAppFlow.from_client_secrets_file(
                    GMAIL_CREDENTIALS_FILE, SCOPES
                )
                self.creds = flow.run_local_server(port=0)
            
            # Save token for future use
            with open(GMAIL_TOKEN_FILE, 'w') as token:
                token.write(self.creds.to_json())
        
        # Build Gmail service
        try:
            self.service = build('gmail', 'v1', credentials=self.creds)
            return True
        except Exception as e:
            print(f"Failed to build Gmail service: {e}")
            return False
    
    def send_email(self, to: str, subject: str, body: str, html: bool = False) -> bool:
        """Send an email via Gmail API"""
        if not self.service:
            if not self.authenticate():
                return False
        
        try:
            message = MIMEMultipart()
            message['to'] = to
            message['subject'] = subject
            
            if html:
                message.attach(MIMEText(body, 'html'))
            else:
                message.attach(MIMEText(body, 'plain'))
            
            raw_message = base64.urlsafe_b64encode(message.as_bytes()).decode()
            
            sent_message = self.service.users().messages().send(
                userId='me',
                body={'raw': raw_message}
            ).execute()
            
            print(f"Email sent successfully. Message ID: {sent_message['id']}")
            return True
            
        except HttpError as error:
            print(f"Gmail API error: {error}")
            return False
    
    def send_task_report(self, task_id: int, objective: str, result: str, recipient: str):
        """Send automated task completion report via email"""
        subject = f"Sentrix Task Report #{task_id}"
        
        html_body = f"""
        <html>
        <body>
            <h2>Sentrix Browser - Task Completion Report</h2>
            <p><strong>Task ID:</strong> {task_id}</p>
            <p><strong>Objective:</strong> {objective}</p>
            <p><strong>Completed At:</strong> {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}</p>
            <hr>
            <h3>Results:</h3>
            <pre>{result}</pre>
            <hr>
            <p><em>Generated by Sentrix Browser Agent</em></p>
        </body>
        </html>
        """
        
        return self.send_email(recipient, subject, html_body, html=True)
    
    def get_recent_emails(self, max_results: int = 10, query: str = "") -> List[dict]:
        """Retrieve recent emails from Gmail"""
        if not self.service:
            if not self.authenticate():
                return []
        
        try:
            results = self.service.users().messages().list(
                userId='me',
                maxResults=max_results,
                q=query
            ).execute()
            
            messages = results.get('messages', [])
            email_list = []
            
            for msg in messages:
                message = self.service.users().messages().get(
                    userId='me',
                    id=msg['id'],
                    format='metadata',
                    metadataHeaders=['From', 'Subject', 'Date']
                ).execute()
                
                headers = {h['name']: h['value'] for h in message['payload']['headers']}
                email_list.append({
                    'id': msg['id'],
                    'from': headers.get('From', ''),
                    'subject': headers.get('Subject', ''),
                    'date': headers.get('Date', '')
                })
            
            return email_list
            
        except HttpError as error:
            print(f"Gmail API error: {error}")
            return []

